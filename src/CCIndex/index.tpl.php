<h2>Installation</h2>
<p>First you have to make the data-directory writable. This is the place where Wooly needs
to be able to write and create files.</p>
<blockquote>
<code>cd Wooly; chmod 777 site/data</code>
</blockquote>

<p>Second, Wooly has some modules that need to be initialised. You can do this through a
controller. Point your browser to the following link.</p>
<blockquote>
<a href='<?=create_url('modules/install')?>'>modules/install</a>
</blockquote>

<p>Now your ready to start using Wooly! Maybe you like to change title and logo? Please check out 
	<code>site/config.php</code></p>
